#include <iostream>
using namespace std;
int main()
{
	int a = 2, b = 3;

	cout << "a = " << a << ", b = " << b << endl;
	cout << "a + b = " << a + b << endl;
	cout << "a - b = " << a - b << endl;
	cout << "a * b = " << a * b << endl;
	cout << "a / b = " << a / b << endl;
	cout << "b % a = " << b % a << endl;
}